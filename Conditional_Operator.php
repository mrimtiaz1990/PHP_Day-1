<?php

//Conditional Operator (<,>,<=,>=,==,!=)
echo "Conditional Operator (<,>,<=,>=,==,!=):", "<br>";

$num1 = 13;
$num2 = 3;

echo "Here, Num-1 = ", $num1, " & <br> Num-2 = ", $num2, "<br><br>";
echo "Less Than (<) = ", $num1 < $num2, "<br>";
echo "Greater Than (>) = ", $num1 > $num2, "<br>";
echo "Less Than Equal (<=) = ", $num1 <= $num2, "<br>";
echo "Greater Than Equal (>=) = ", $num1 >= $num2, "<br>";
echo "Equal (=) = ", $num1 == $num2, "<br>";
echo "Not Equal (!=) = ", $num1 != $num2;

?>
